import mongoose from 'mongoose'

const orderSchema = new mongoose.Schema({
    files: {
        type: Array,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        required: true,
        default: Date.now
    }
})

export default mongoose.model('Order', orderSchema)